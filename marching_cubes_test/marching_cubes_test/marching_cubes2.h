int triTableValue(int i, int j)
{
	if (i >= 256 || j >= 16)
		return -1;

	return triangleTable1[i][j];
}

// Trilinear vertex interpolation, from Paul Bourke's reference implementation
Vector3 VertexInterp(float isoValue, Vector3 p1, Vector3 p2, float valp1, float valp2)
{
	float mu = 0.0f;
	Vector3 p = { 0.0f, 0.f, 0.f };
	if (abs(isoValue - valp1) < 0.00001)
		return(p1);
	if (abs(isoValue - valp2) < 0.00001)
		return(p2);
	if (abs(valp1 - valp2) < 0.00001)
		return(p1);

	mu = (isoValue - valp1) / (valp2 - valp1);
	p.x = p1.x + mu * (p2.x - p1.x);
	p.y = p1.y + mu * (p2.y - p1.y);
	p.z = p1.z + mu * (p2.z - p1.z);
	return(p);
}

// Calculate a new normal vertex by sampling either side of the cell in each dimension
Vector3 CalculateNormal(Vector3 position)
{
	float normal[3];
	// Initialise values for fractional Brownian motion
	normal[0] = 0.0f;
	normal[1] = 0.0f;
	normal[2] = 0.0f;

	//normal[0] = noiseTexture.SampleLevel(sampleType, float3(position.x + 1.0f, position.y, position.z) / meshSize, 0) - noiseTexture.SampleLevel(sampleType, float3(position.x - 1.0f, position.y, position.z) / meshSize, 0);
	//normal[1] = noiseTexture.SampleLevel(sampleType, float3(position.x, position.y + 1.0f, position.z) / meshSize, 0) - noiseTexture.SampleLevel(sampleType, float3(position.x, position.y - 1.0f, position.z) / meshSize, 0);
	//normal[2] = noiseTexture.SampleLevel(sampleType, float3(position.x, position.y, position.z + 1.0f) / meshSize, 0) - noiseTexture.SampleLevel(sampleType, float3(position.x, position.y, position.z - 1.0f) / meshSize, 0);

	float length = sqrt(normal[0] * normal[0] + normal[1] * normal[1] + normal[2] * normal[2]);
	normal[0] /= length;
	normal[1] /= length;
	normal[2] /= length;

	return { normal[0], normal[1], normal[2] };
}

#define RADIUS 5.0f

float SampleValue(Vector3 pos)
{
	return VectorDistance({ 0.f, 0.f, 0.f }, pos) - RADIUS;
}

